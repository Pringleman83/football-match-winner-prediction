    private void jRadioButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton1ActionPerformed
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        
        try {
                POIFSFileSystem fs = new POIFSFileSystem(new FileInputStream("//home//saurabh//Football//" + Integer.toString(Row)+".xlsx"));
                HSSFWorkbook wb = new HSSFWorkbook(fs);
                HSSFSheet sheet = wb.getSheetAt(0);
                HSSFRow row=sheet.getRow(sheet.getFirstRowNum());;
                HSSFCell cell;
                
                int rows; // No of rows
                rows = sheet.getPhysicalNumberOfRows();

                int cols = 4; // No of columns
                int i,j=0;
                
                for(i=rows-33;i<rows;i++,j++){
                    row = sheet.getRow(i);
                    Cell c = row.getCell(0);
                    if((j%2==0 && j>=2) || j==0){
                        dataset.setValue(c.getNumericCellValue(),"",Integer.toString(j));
                    }
                    else{
                        dataset.setValue(c.getNumericCellValue(),"","");
                    }
                    
                }     
                JFreeChart line = ChartFactory.createLineChart("Form", "Matches", "Value", dataset);
                CategoryPlot linegraph = line.getCategoryPlot();
                linegraph.setRangeGridlinePaint(Color.orange);

                ChartFrame frame = new ChartFrame("Line graph",line);
                frame.setVisible(true);
                frame.setSize(600, 600);
        }
        catch(Exception ioe) {
            ioe.printStackTrace();
        }
        
    }//GEN-LAST:event_jRadioButton1ActionPerformed

